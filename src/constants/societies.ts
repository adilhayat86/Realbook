// Helper: Get all societies for a city
export const getSocieties = (city: "Rawalpindi" | "Islamabad"): string[] => {
  return Object.keys(TWIN_CITIES_AREAS[city]);
};

// Helper: Get phases/sectors for a society
export const getPhasesForSociety = (
  city: "Rawalpindi" | "Islamabad",
  society: string
): string[] => {
  const cityData = TWIN_CITIES_AREAS[city] as any;
  const societyData = cityData[society];
  if (!societyData) return [];
  return Object.keys(societyData);
};

// Helper: Get blocks for a phase
export const getBlocksForPhase = (
  city: "Rawalpindi" | "Islamabad",
  society: string,
  phase: string
): string[] => {
  const cityData = TWIN_CITIES_AREAS[city] as any;
  return cityData[society]?.[phase] || [];
};

